package datatypes;

public class Intersection {
	 public float cx, cy, time, nx, ny, ix, iy;
     public Intersection(float x, float y, float time, float nx, float ny, float ix, float iy)
     {
         this.cx = x;
         this.cy = y;
         this.time = time;
         this.nx = nx;
         this.ny = ny;
         this.ix = ix;
         this.iy = iy;
     }
}
